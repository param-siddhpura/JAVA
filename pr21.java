import java.util.*;
//Bitwise Complement operation
public class pr21 {
  public static void main(String[] args){
    Scanner s= new Scanner(System.in);
    System.out.println("Enter First Number:-");
    int a= s.nextInt();
  
    int finalResult= ~ a;
    System.out.println("Final result is :- "+ finalResult);
  }
  
}

