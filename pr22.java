import java.util.*;
//Bitwise left Shift operation
public class pr22 {
  public static void main(String[] args){
    Scanner s= new Scanner(System.in);
    System.out.println("Enter First Number:-");
    int a= s.nextInt();
  
    int finalResult= a << 1;
    System.out.println("Final result is :- "+ finalResult);
  }
  
}
